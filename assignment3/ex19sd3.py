def grumble(pug_count,extra_pugs):
	print(f"You have {pug_count} pugs in your house!") 
	print("Damn son! That's almost a grumble!") 
	print(f"If you had {pug_count} pugs plus {extra_pugs} more pugs, you would have a grumble!")

print("Lets talk about pugs.") 
grumble(2,1)

print("What if we had more pugs?")

pug_count = 20
extra_pugs = 100 

grumble(pug_count,extra_pugs)

print("Nah, dawg, that's a pug-apalooza!")


